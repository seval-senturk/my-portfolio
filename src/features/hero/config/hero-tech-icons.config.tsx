export function isHeroTechIconPreset(icon: string): boolean {
  return HERO_TECH_ICON_OPTIONS.some((option) => option.value === icon);
}

export function isHeroTechIconUrl(icon: string): boolean {
  const value = icon.trim();
  if (!value || isHeroTechIconPreset(value)) {
    return false;
  }

  return (
    value.startsWith("http://") ||
    value.startsWith("https://") ||
    value.startsWith("/") ||
    value.includes("uploads/") ||
    /\.(png|jpe?g|webp|gif|avif|svg)(\?.*)?$/i.test(value)
  );
}

export function normalizeHeroTechIconUrl(icon: string): string {
  const value = icon.trim();
  if (!isHeroTechIconUrl(value)) {
    return value;
  }

  if (
    value.startsWith("http://") ||
    value.startsWith("https://") ||
    value.startsWith("/")
  ) {
    return value;
  }

  return `/${value.replace(/^\/+/, "")}`;
}

export function resolveHeroTechIcon(icon: string): string {
  return isHeroTechIconPreset(icon) ? icon : "react";
}

interface HeroTechIconProps {
  icon: string;
  className?: string;
}

export function HeroTechIcon({ icon, className }: HeroTechIconProps) {
  if (isHeroTechIconUrl(icon)) {
    return (
      // eslint-disable-next-line @next/next/no-img-element
      <img
        src={normalizeHeroTechIconUrl(icon)}
        alt=""
        className={cn("h-5 w-5 object-contain", className)}
        aria-hidden
      />
    );
  }

  const key = resolveHeroTechIcon(icon);
  const Icon = ICON_MAP[key] ?? ReactIcon;
  return <span className={cn("inline-flex", className)}>{Icon()}</span>;
}
